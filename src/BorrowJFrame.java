    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int borrowID;
        try {
            borrowID = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Book ID must be a number!");
            return;
        }
        if (!service.borrowExists(borrowID)) {
            JOptionPane.showMessageDialog(this, " ID not found!");
            return;

        }
        try {

            service.deleteBorrowing(borrowID);

            loadBorrowTable();
            JOptionPane.showMessageDialog(this, "deleted Successfully!");
            jTextField1.setText("");
            jTextField6.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "un available id!");
            
        }


    }//GEN-LAST:event_btndeleteActionPerformed

