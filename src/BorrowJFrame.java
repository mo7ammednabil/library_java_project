    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:                                       
        try {

            int bookID = Integer.parseInt(jTextField6.getText());
            String studentName = "";
            int studentID = Integer.parseInt(jTextField5.getText());
            LocalDate dateBorrow = LocalDate.parse(jTextField3.getText());
            LocalDate dateReturn = LocalDate.parse(jTextField4.getText());

            if (!service.bookExists(bookID)) {
                JOptionPane.showMessageDialog(this, "Book ID does not exist!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!service.studentExists(studentID)) {
                JOptionPane.showMessageDialog(this, "Student ID does not exist!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Borrowing borrow = new Borrowing(0, bookID, studentID, studentName, dateBorrow, dateReturn);
            service.addBorrowing(borrow);

            loadBorrowTable();
            JOptionPane.showMessageDialog(this, "Inserted Successfully!");
            jTextField1.setText("");
            jTextField6.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Enter all data", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btninsertActionPerformed

