    private void loadBorrowTable() {
        model.setRowCount(0);
        for (Borrowing b : service.getAllBorrowing()) {
            model.addRow(new Object[]{
                b.getBorrowID(),
                b.getBookID(),
                b.getStudentName(),
                b.getStudentID(),
                b.getDateBorrowing(),
                b.getDateReturn()
            });
        }
    }

