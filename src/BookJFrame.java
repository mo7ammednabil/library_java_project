    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        int id ;
        String name = jTextField2.getText();
        String author = jTextField3.getText();
        String selected = jComboBox1.getSelectedItem().toString();
        int categoryID = 0;
       try {
            id = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Book ID must be a number!");
            return;
        }

        if (service.bookExists(id)) {
            JOptionPane.showMessageDialog(this, "Book ID exist already!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        switch (selected) {
            case "History":
                categoryID = 1;
                break;
            case "Programming":
                categoryID = 2;
                break;
            case "Science":
                categoryID = 3;
                break;
            case "Math":
                categoryID = 4;
                break;
        }

        Book b = new Book(id, name, author, categoryID);
        service.addBook(b);
        JOptionPane.showMessageDialog(this, "Added Successfully!");

        loadBooksTable();

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");


    }//GEN-LAST:event_btninsertActionPerformed

