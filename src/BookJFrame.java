    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:

        
        int id ;
        try {
            id = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Book ID must be a number!");
            return;
        }
         if (service.checkBookBorrowed(id)) {
              JOptionPane.showMessageDialog(this, "Book is been borrowing , can't delete");
        
        return ;
    }
        if (!service.bookExists(id)) {
            JOptionPane.showMessageDialog(this, "Book ID not found!");
            return;
        }
        service.deleteBook(id);
        JOptionPane.showMessageDialog(this, "deleted Successfully!");
        loadBooksTable();
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }//GEN-LAST:event_btndeleteActionPerformed
    private void loadBooksTable() {
        model.setRowCount(0);

        for (Book b : service.getAllBooks()) {
            model.addRow(new Object[]{b.getBookID(), b.getBookName(), b.getAuthor(), b.getCategory()});

        }
    }

